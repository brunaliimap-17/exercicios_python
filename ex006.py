num = int(input('Digite um número:'))

print('O dobro de {} é {}'.format(num,(num*2)))
print('O triplo de {} é {}'.format(num,(num*3)))
print('A raiz quadrada de {} é {:.3f}'.format(num,(num**(1/2))))
print('A raiz cubica de {} é {:.3f}'.format(num,(num**(1/3))))
print('Elevado ao quadrado é {}'.format(num**2))

